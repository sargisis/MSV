#include "ALU.h"

double ALU::adder(double first, double second)
{
    return first + second;
}

double ALU::subtract(double first, double second)
{
    return first - second;
}

double ALU::multiply(double first, double second)
{
    return first * second;
}

double ALU::divider(double first, double second)
{
    return first / second;
}

double ALU::modulo(int first, int second)
{
    return first % second;
}